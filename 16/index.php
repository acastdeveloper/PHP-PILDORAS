  <h1>16. Operador Ternari</h1>

  <pre>
    Condició? Valor si és TRUE : Valor si és FALSE.
  </pre>

<p><strong>Exemple:</strong></p>
<pre>
    $edad=3;
    echo $edad &lt; 18 ? "Ets menor d'edat." : "Ets adult.";
    // "Ets menor d'edat"
</pre>

